game.onUpdate(function () {
    // Handle acceleration when UP is pressed
    if (controller.up.isPressed() && speed2 < 370) {
        speed2 += 0.3
    } else {
        // Natural coasting deceleration
        if (speed2 > 2) {
            speed2 += 0 - 0.1
        } else {
            speed2 = 0
        }
    }
    // Compute target velocities based on direction and speed
    ProperVX = Render.getAttribute(Render.attribute.dirX) * speed2
    ProperVY = Render.getAttribute(Render.attribute.dirY) * speed2
    // Handle braking (linear reduction of velocity)
    if (controller.A.isPressed()) {
        // Reduce velocity linearly
        if (OldVX > 0) {
            OldVX += 0 - brakePower
            if (OldVX < 0) {
                OldVX = 0
            }
        } else if (OldVX < 0) {
            OldVX += brakePower
            if (OldVX > 0) {
                OldVX = 0
            }
        }
        if (OldVY > 0) {
            OldVY += 0 - brakePower
            if (OldVY < 0) {
                OldVY = 0
            }
        } else if (OldVY < 0) {
            OldVY += brakePower
            if (OldVY > 0) {
                OldVY = 0
            }
        }
        // Decrease speed2 during brake
        speed2 += 0 - 1
        if (speed2 < 0) {
            speed2 = 0
        }
    } else {
        // Approach target velocities smoothly (acceleration + coasting)
        OldVX += (ProperVX - OldVX) * 0.1
        OldVY += (ProperVY - OldVY) * 0.1
    }
    // Apply velocities to your sprite
    mySprite5.vx = OldVX
    mySprite5.vy = OldVY
    // Optional steering: rotate sprite with LEFT/RIGHT
    if (controller.left.isPressed()) {
        transformSprites.rotateSprite(mySprite8, -2)
    } else if (controller.right.isPressed()) {
        transformSprites.rotateSprite(mySprite8, 2)
    }
    // Rotate speedometer needle based on current speed
    transformSprites.rotateSprite(mySprite8, Math.map(speed2, 0, 370, 0, 182))
})
